package com.example.javaproject2.codeup.day11;

import java.util.Scanner;

public class Codeup1071 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int input = 0;
        for (;;) {
            input = sc.nextInt();

            if(input == 0) break;
            System.out.println(input);
        }
    }
}
